// Helper function to get random message from array
export const getRandomMessage = (messages: string[]): string => {
  return messages[Math.floor(Math.random() * messages.length)];
};

// Helper function to get time-appropriate greeting
export const getTimeBasedGreeting = (): string => {
  const hour = new Date().getHours();
  
  if (hour >= 5 && hour < 12) {
    return getRandomMessage(lunaMessages.dailyGreeting.morning);
  } else if (hour >= 12 && hour < 17) {
    return getRandomMessage(lunaMessages.dailyGreeting.afternoon);
  } else if (hour >= 17 && hour < 21) {
    return getRandomMessage(lunaMessages.dailyGreeting.evening);
  } else {
    return getRandomMessage(lunaMessages.dailyGreeting.night);
  }
};

// Helper function to get streak message
export const getStreakMessage = (streak: number): string | null => {
  if (streak >= 30) {
    return getRandomMessage(lunaMessages.streakCelebration.streak30);
  } else if (streak >= 14) {
    return getRandomMessage(lunaMessages.streakCelebration.streak14);
  } else if (streak >= 7) {
    return getRandomMessage(lunaMessages.streakCelebration.streak7);
  } else if (streak >= 3) {
    return getRandomMessage(lunaMessages.streakCelebration.streak3);
  }
  return null;
};

// Helper function to get page-specific message
export const getPageMessage = (page: keyof typeof lunaMessages.pageContext): string => {
  const messages = lunaMessages.pageContext[page];
  return messages ? getRandomMessage(messages) : getRandomMessage(lunaMessages.encouragement);
};
